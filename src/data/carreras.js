export const getCarreraById = (id) =>
    carreras.find((c) => c.id === id);

export const getMateriasByCarreraYAño = (carreraId, añoMaximo) => {
    const carrera = getCarreraById(carreraId);
    if (!carrera) return [];

    // Si no se especifica añoMaximo, retornamos todas las materias
    if (!añoMaximo) return carrera.materias;

    // Filtramos para retornar sólo las materias hasta el año seleccionado
    const maxYear = parseInt(añoMaximo, 10);
    return carrera.materias.filter((materia) => materia.año <= maxYear);
};
